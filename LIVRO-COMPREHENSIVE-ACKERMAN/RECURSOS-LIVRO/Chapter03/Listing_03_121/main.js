'use strict';
function printPersonInformation(firstName, lastName, age) {
  console.log(`First name: ${firstName}`);
  console.log(`Last name: ${lastName}`);
  console.log(`Age: ${age}`);
}

printPersonInformation('John', 'Doe', 44);
printPersonInformation('James', 'Doe', 55);

// Output:
// First name: John
// Last name: Doe
// Age: 44
// First name: James
// Last name: Doe
// Age: 55
